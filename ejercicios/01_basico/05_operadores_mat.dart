/**
 *    Un operador es un símbolo que le dice al compilador
 *    qué debe realizar una tarea
 *    matemática, relacional o lógica
 *    y debe de producir un resultado
 * 
 */

main() {

  int a;
  a = 10 + 5;   // Adición
  a = 20 - 10;  // Sustración
  a = 10 * 2;   // Producto

  double b;
  b = 10 / 2 ;   // División
  b = 10.0 % 3 ; // Modulo (Resto)
  b = -b;        // Negación

  int c;
  c = 10 ~/ 3;   // Cociente ( Solo la parte entera )

  int d = 1;     // Asignación

  d++ ;           // Post Incremento +1
  d-- ;           // Post Decremento -1
}